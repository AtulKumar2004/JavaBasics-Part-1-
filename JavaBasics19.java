// Relational operators
public class JavaBasics19 {
    public static void main(String args[]){
        int A=10;
        int B=10;
        System.out.println(A==B);
        System.out.println(A!=B);
        System.out.println(B>A);
        System.out.println(A<B);
        System.out.println(A>=B);
        System.out.println(A<=B);
    }
}