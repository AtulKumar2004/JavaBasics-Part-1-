// switch statement
public class JavaBasics31 {
    public static void main(String args[]){
        char ch='b';
        switch (ch){
            case 'd': System.out.println("samosa");
                      break;
            case 'a': System.out.println("burger");
                      break;
            case 'b': System.out.println("mango juice");
                      break;
            default: System.out.println("we wake up");

        }
    }
}
